
using Microsoft.EntityFrameworkCore;
using SportsStore.Models;

var builder = WebApplication.CreateBuilder(args); // making web app

builder.Services.AddDbContext<StoreDbContext>(opts => {
    opts.UseSqlServer(builder.Configuration["ConnectionStrings:SportsStoreConnection"]!);
});

builder.Services.AddControllersWithViews();

builder.Services.AddScoped<IStoreRepository, EFStoreRepository>(); // inversion of control

var app = builder.Build();

//middlewares
app.UseStaticFiles();

app.MapControllerRoute("pagination","Products/Page{productPage}",
new { Controller = "Home", action = "Index" }); // naay paging

app.MapDefaultControllerRoute(); // default
SeedData.EnsurePopulated(app);
app.Run();




























































































































































































































































































































